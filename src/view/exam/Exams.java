    private void btn_CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CreateActionPerformed
        // TODO add your handling code here:

        String classYear = txtClassYear.getText();
        String examID = examIDGenerater.createExamID(classYear, examController.searchExamCount(classYear));
        Date date = new java.sql.Date(txtExamDate.getDate().getTime());
        Exam exam = new Exam(examID, date, txaDescription.getText(), classYear, examFeeTxtFld.getText());
        examController.addExam(exam);
        updateTable();
        txtClassYear.setText("");
        txtExamDate.setDate(null);
        txaDescription.setText("");
    }//GEN-LAST:event_btn_CreateActionPerformed

    private void tbl_ExamsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ExamsMouseClicked
        // TODO add your handling code here:
        int row = tbl_Exams.getSelectedRow();
        String clickData = tbl_Exams.getModel().getValueAt(row, 0).toString();
        try {
            ResultSet rst = examController.searchExam(clickData);
            if (rst.next()) {
                txtExamID.setText(rst.getString("examID"));
                txtExamDate2.setDate(rst.getDate("date"));
                txaDescription2.setText(rst.getString("description"));
            } else {
                JOptionPane.showMessageDialog(null, "cannot found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tbl_ExamsMouseClicked

