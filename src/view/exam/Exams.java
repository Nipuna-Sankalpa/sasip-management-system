    private void cmbExamIDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbExamIDItemStateChanged
        // TODO add your handling code here:
        String examID = "";
        if (cmbExamID.getSelectedItem() != null) {
            examID = cmbExamID.getSelectedItem().toString();
            if (!examID.equals("No such exam")) {
                try {
                    ResultSet rst = examController.searchExam(cmbExamID.getSelectedItem().toString());
                    if (rst.next()) {
                        txtExamID.setText(rst.getString("examID"));
                        txtExamDate2.setDate(rst.getDate("date"));
                        txaDescription2.setText(rst.getString("description"));
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }

    }//GEN-LAST:event_cmbExamIDItemStateChanged
